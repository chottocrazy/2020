<h1><b>About</b><br/>「ラ(だと思う音)」の音合わせ<br/></h1>
<p>
  <br/>
  <b>大 chotto crazy 2020 公式プログラム</b>
</p>
<p>オーケストラやアカペラが音合わせをするように、みんながお互いの音を聴き、合わせられるようになったら何か起こるんじゃないか。<br/>そんな願いを込め、実際に何人かで「ラ（だと思う音）」の音合わせをしたい。</p>
<p>難しいかもしれないし、簡単かもしれない。<br/>
けれどバラバラな私たちが、それぞれの声で音を合わせる。<br/>
それはまるで、平和な世界への第一歩のような...<br/>
そんな思いからこの企画が始まりました。</p>
